    //enter farmer id code to fetch related details
    private void jTextField46KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField46KeyReleased
        // TODO add your handling code here:
            String fid = jTextField46.getText().trim();

    if (fid.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter Farmer ID.");
        return;
    }

    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // clear previous rows

    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost/pomegranate", "root", "");

        // 1. Get farmer details
        String farmerQuery = "SELECT name, mobile1, address FROM farmer_registration WHERE fid = ?";
        PreparedStatement farmerPst = con.prepareStatement(farmerQuery);
        farmerPst.setString(1, fid);
        ResultSet farmerRs = farmerPst.executeQuery();

        if (farmerRs.next()) {
            jTextField8.setText(farmerRs.getString("name"));
            jTextField50.setText(farmerRs.getString("mobile1"));
            jTextField49.setText(farmerRs.getString("address"));
        } else {
            JOptionPane.showMessageDialog(this, "Farmer ID not found.");
            return;
        }

        // 2. Get all pomegranates sold by this farmer
        String query = "SELECT p.variety, p.size, p.weight, s.price, s.total, t.name AS trader_name " +
                       "FROM sells s " +
                       "JOIN pomegranates p ON s.fid = p.fid " +
                       "JOIN trader_registration t ON s.tid = t.tid " +
                       "WHERE s.fid = ?";

        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, fid);
        ResultSet rs = pst.executeQuery();

        int serialNo = 1;
        while (rs.next()) {
            String variety = rs.getString("variety");
            String size = rs.getString("size");
            String weight = rs.getString("weight");
            String price = rs.getString("price");
            String total = rs.getString("total");
            String traderName = rs.getString("trader_name");

            model.addRow(new Object[]{
                serialNo++, variety, size, weight, price, total, traderName
            });
        }

        // 3. Fill up to 7 rows with default empty values
        while (model.getRowCount() < 7) {
            int sr = model.getRowCount() + 1;
            model.addRow(new Object[]{sr, "", "Not Provided", "0", "0", "0", ""});
        }

        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jTextField46KeyReleased

