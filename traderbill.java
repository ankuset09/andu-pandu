    private int findNextFreeRow(DefaultTableModel model) {
    for (int i = 0; i < model.getRowCount(); i++) {
        Object priceCell = model.getValueAt(i, 4);
        if (priceCell == null) return i;
        String txt = priceCell.toString().trim();
        if (txt.isEmpty() || txt.equals("0")) return i;
    }
    return (model.getRowCount() < 7) ? model.getRowCount() : -1;
}
    
    
