    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            double amount = Double.parseDouble(jTextField45.getText().trim());  // Total amount
            double commission = amount * 0.02;    // 2%
            double labour = amount * 0.005;       // 0.5%
            double measure = amount * 0.001;      // 0.1%

            // Read motor and advance safely
            double motor = Double.parseDouble(jTextField40.getText().trim());   // Motor charge
            double advance = Double.parseDouble(jTextField42.getText().trim()); // Advance payment

            // Set individual charges in text fields
            jTextField39.setText(String.format("%.2f", commission));
            jTextField9.setText(String.format("%.2f", labour));
            jTextField51.setText(String.format("%.2f", measure));

            // Calculate and display total deductions
            double totalcost = commission + labour + measure + motor + advance;
            jTextField47.setText(String.format("%.2f", totalcost));

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "❌ Please ensure all input fields contain valid numbers.");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

