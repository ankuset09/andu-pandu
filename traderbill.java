    private void jTextField46KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField46KeyReleased
        // TODO add your handling code here:
          String tid = jTextField46.getText().trim();

    if (tid.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter Trader ID.");
        return;
    }

    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Clear table

    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost/pomegranate", "root", "");

        // 1. Get trader details
        String traderQuery = "SELECT name, mobile1, address FROM trader_registration WHERE tid = ?";
        PreparedStatement traderPst = con.prepareStatement(traderQuery);
        traderPst.setString(1, tid);
        ResultSet traderRs = traderPst.executeQuery();

        if (traderRs.next()) {
            jTextField8.setText(traderRs.getString("name"));
            jTextField50.setText(traderRs.getString("mobile1"));
            jTextField49.setText(traderRs.getString("address"));
        } else {
            JOptionPane.showMessageDialog(this, "Trader ID not found.");
            return;
        }

        // 2. Fetch billing details from related tables
        String query = "SELECT p.variety, p.size, p.weight, s.price, s.total, f.name AS farmer_name " +
                       "FROM sells s " +
                       "JOIN pomegranates p ON s.fid = p.fid AND s.tid = p.tid " +
                       "JOIN farmer_registration f ON s.fid = f.fid " +
                       "WHERE s.tid = ?";

        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, tid);
        ResultSet rs = pst.executeQuery();

        int sr = 1;
        while (rs.next()) {
            String variety = rs.getString("variety");
            String size = rs.getString("size");
            String weight = rs.getString("weight");
            String price = rs.getString("price");
            String total = rs.getString("total");
            String farmerName = rs.getString("farmer_name");

            model.addRow(new Object[]{
                sr++, variety, size, weight, price, total, farmerName
            });
        }

        // Fill up to 7 rows
        while (model.getRowCount() < 7) {
            model.addRow(new Object[]{
                model.getRowCount() + 1, "", "Not Provided", "0", "0", "0", ""
            });
        }

        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jTextField46KeyReleased

