    private void loadFarmerNames() {
    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost/pomegranate", "root", "");
        String query = "SELECT DISTINCT name FROM farmer_registration";
        PreparedStatement pst = con.prepareStatement(query);
        ResultSet rs = pst.executeQuery();

        jComboBox1.removeAllItems();
        while (rs.next()) {
            jComboBox1.addItem(rs.getString("name"));
        }

        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading farmer names: " + e.getMessage());
    }
}

private void loadFarmerIDs(String farmerName) {
    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost/pomegranate", "root", "");
        String query = "SELECT fid FROM farmer_registration WHERE name = ?";
        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, farmerName);
        ResultSet rs = pst.executeQuery();

        jComboBox3.removeAllItems();
        while (rs.next()) {
            jComboBox3.addItem(rs.getString("fid"));
        }

        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading farmer IDs: " + e.getMessage());
    }
}

    
    
