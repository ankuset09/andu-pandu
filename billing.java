    //farmer ID textfield
    private void jTextField3KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField3KeyReleased
        // TODO add your handling code here:
         // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
    // then write the code inside this block
    String farmerId = jTextField3.getText();

    // Only proceed if ID is at least 1 digit
    if (farmerId.length() == 0) return;

    try {
        String url = "jdbc:mysql://localhost/pomegranate";
        String user = "root";
        String pass = ""; 

        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection(url, user, pass);

        String query = "SELECT name, mobile1, address FROM farmer_registration WHERE fid = ?";
        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, farmerId);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            jTextField14.setText(rs.getString("name"));
            jTextField1.setText(rs.getString("mobile1"));
            jTextField2.setText(rs.getString("address"));
        } else {
            jTextField14.setText("");
            jTextField1.setText("");
            jTextField2.setText("");
        }

        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}
    }//GEN-LAST:event_jTextField3KeyReleased

    //save button code for pomegranates table
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
                                      
    String url  = "jdbc:mysql://localhost:3306/pomegranate?serverTimezone=UTC";
    String user = "root";
    String pass = "";

    String sql  = "INSERT INTO pomegranates (variety, size, weight, fid, tid) VALUES (?,?,?,?,?)";

    try (Connection con = DriverManager.getConnection(url, user, pass);
         PreparedStatement pst = con.prepareStatement(sql)) {

        pst.setString(1, jTextField13.getText().trim());                 // variety
        pst.setString(2, jComboBox2.getSelectedItem().toString());       // size
        // if weight is numeric, store it as number instead of text
        BigDecimal wt = new BigDecimal(jTextField4.getText().trim());
        pst.setBigDecimal(3, wt);                                        // weight
          pst.setString(4, jTextField3.getText().trim());      //farmer id
           pst.setString(5, jTextField15.getText().trim());      //trader id

        int rows = pst.executeUpdate();           // 🔑 actually runs the INSERT

        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "✅ Saved successfully");
        } else {
            JOptionPane.showMessageDialog(this, "⚠️ Nothing inserted");
        }

    } catch (Exception ex) {
        ex.printStackTrace();     // see the real cause in Output window
        JOptionPane.showMessageDialog(this,
                "❌ Error while saving: " + ex.getMessage(),
                "Database Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_jButton1ActionPerformed

